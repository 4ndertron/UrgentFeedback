WITH ACCOUNT_LIST AS (
    SELECT REGEXP_SUBSTR(A.ATTACHMENT_NAME, '\\d{6}s|\\d{6}|\\d{7}', 1, 1, 'ei') AS SERVICE_NUMBER
         , TO_DATE(A.CREATED_DATE)                                                  DAY_UPLOADED
    FROM RPT.T_ATTACHMENT AS A
    WHERE A.UPLOADED_BY_NAME ILIKE '%LD_FTP%'
)

SELECT *
FROM ACCOUNT_LIST
